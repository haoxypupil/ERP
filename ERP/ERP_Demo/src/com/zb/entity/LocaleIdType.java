package com.zb.entity;

public class LocaleIdType {
	private Integer idTypeId;
	private String idTypeNum;
	private String idTypeName;
	
	public Integer getIdTypeId() {
		return idTypeId;
	}
	public void setIdTypeId(Integer idTypeId) {
		this.idTypeId = idTypeId;
	}
	public String getIdTypeNum() {
		return idTypeNum;
	}
	public void setIdTypeNum(String idTypeNum) {
		this.idTypeNum = idTypeNum;
	}
	public String getIdTypeName() {
		return idTypeName;
	}
	public void setIdTypeName(String idTypeName) {
		this.idTypeName = idTypeName;
	}
	
}
