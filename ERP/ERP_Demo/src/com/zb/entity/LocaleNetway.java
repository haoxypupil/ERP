public class LocaleNetway {
	private Integer netwayId;
	private String netwayName;
	
	
	public Integer getNetwayId() {
		return netwayId;
	}
	public void setNetwayId(Integer netwayId) {
		this.netwayId = netwayId;
	}
	public String getNetwayName() {
		return netwayName;
	}
	public void setNetwayName(String netwayName) {
		this.netwayName = netwayName;
	}
	
}
